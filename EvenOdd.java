package labsession.lab18072025;
public class EvenOdd {
    public static void EvenOdd(String[] args) {
        System.out.print("Enter a number: ");
        
        int num = 0;
        if (num % 2 == 0)
            System.out.println(num + " is Even.");
        else
            System.out.println(num + " is Odd.");
    }
}
