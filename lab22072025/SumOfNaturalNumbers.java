package corejava;



public class SumOfNaturalNumbers {

    public static void main(String[] args) {
       

        // Input: Enter the value of N
        System.out.print("Enter the value of N: ");
        

        int sum = 0;
        int i = 1;

        // While loop to calculate sum
        while (i <= sum) {
            sum += i;
            i++;
        }

        
        System.out.println("Sum of first " + sum + " natural numbers is: " + sum);

        
}
}
