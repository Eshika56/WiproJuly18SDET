package corejava;

public class NumberTriangle {

	public static void main(String[] args) {
		System.out.println("Input number of rows:");
		int rows = 10;
		for (int i = 1; i<= rows; i++) {
			for (int j = 1; j<= i; j++) {
				System.out.println(j);
			}
			System.out.println(rows);
		
			
		}
		

	
	

}}
